package com.spring.model;

public class MarkSheet {

	private String studentName;
	private int marksInMath;
	private int marksInPhysics;
	private int marksInChemistry;

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public int getMarksInMath() {
		return marksInMath;
	}

	public void setMarksInMath(int marksInMath) {
		this.marksInMath = marksInMath;
	}

	public int getMarksInPhysics() {
		return marksInPhysics;
	}

	public void setMarksInPhysics(int marksInPhysics) {
		this.marksInPhysics = marksInPhysics;
	}

	public int getMarksInChemistry() {
		return marksInChemistry;
	}

	public void setMarksInChemistry(int marksInChemistry) {
		this.marksInChemistry = marksInChemistry;
	}

}
